package com.mygdx.game.data;

public class TileData {
    private float x;
    private float y;

    public TileData(float x, float y) {
        this.x = x;
        this.y = y;
    }

    // GETTERS

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
